package algorithms

// SelectionSort rearranges the array in ascending order, using the selection sort algorithm.
func SelectionSort(a []int) {
	n := len(a)
	for i := 0; i < n; i++ {
		min := i
		for j := i + 1; j < n; j++ {
			if Less(a[j], a[min]) {
				min = j
			}
		}
		Exch(a, i, min)
	}
}
